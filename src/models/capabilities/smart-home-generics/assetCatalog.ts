export class AssetCatalog {
  static getAssetIdentifiers() {
    return alexaDefinedAssets.map((asset) => asset.assetIdentifier);
  }

  static getAssetCatalog() {
    return alexaDefinedAssets;
  }

  static getAssetCategories() {
    let categories = {};
    assetCatalog.forEach((asset) => {
      let category = asset.assetId.split(".")[1];
      if (Array.isArray(categories[category])) {
        categories[category].push(asset);
      } else {
        categories[category] = [];
        categories[category].push(asset);
      }
    });
    return categories;
  }

  static getUnitsOfMeasurement() {
    let unitsOfMeasurement = [];
    let unitAssets = assetCatalog.filter(
      (asset) => asset.assetId.split(".")[1] === "Unit"
    );
    return unitAssets.map((unit) => unit.assetId);
  }
}
